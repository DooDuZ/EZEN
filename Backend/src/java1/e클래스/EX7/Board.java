public class Board {

	// 필드
	String title ;
	String content ;
	String writer ;
	String password ;
	int view ;
	
	// 생성자 
	
	public Board() {}
	Board( String title, String content, String writer, String password, int view){
		this.title = title;
		this.content = content;
		this.writer = writer;
		this.password = password;		
		this.view = view;
	}
	
	
	
	// 메소드
	
}
